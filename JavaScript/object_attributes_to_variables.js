// assigning object attributes to variables
const person = {
    name: 'Affan Ali',
    age: 23,
    gender: 'Male',
    country: 'Pakistan',
}

let name = person.name;
let age = person.age;
let gender = person.gender;
let country = person.country;

console.log('Name: '+name); // Affan Ali
console.log('Age: '+age); // 23
console.log('Gender: '+gender); // Male
console.log('Country: '+country); // Pakistan